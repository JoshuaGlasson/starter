function doMaths(num) {
    console.log("I did some maths!");

    switch (num) {
        case 0:
            console.log("3 + 3 = " + (3 + 3));
            break;
        case 1:
            console.log("3 x 3 = " + (3 * 3));
            break;
        case 2:
            console.log("3 / 3 = " + (3 / 3));
            break;
        case 3:
            console.log("3 - 3 = " + (3 - 3));
            break;
    }
}